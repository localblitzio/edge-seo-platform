#!/usr/bin/env node
/**
 * Re-generates the inlined logo + favicon data-URL modules from
 * `assets/edgeseo-logo.png` and `assets/edgeseo-favicon.png`. Run when
 * either image changes.
 *
 * Why generated TS modules instead of static asset bindings?
 *   - Wrangler doesn't bundle binary file imports natively in Workers.
 *   - A generated TS string keeps the data in source where it's easy
 *     to diff, audit, and bundle.
 *   - Both PNGs are small (resized to ~34 KB / ~7 KB) so the bundle
 *     hit is negligible and saves an HTTP round-trip per page load.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const root = resolve(here, "..");

function generate({ srcPng, outTs, exportName, sourceLabel }) {
  const png = readFileSync(srcPng);
  const dataUrl = `data:image/png;base64,${png.toString("base64")}`;
  const body = `/**
 * Generated by \`scripts/regen-logo-data-url.mjs\` from
 * \`${sourceLabel}\`. Do not edit by hand — re-run the script
 * to refresh after changing the image.
 *
 * Inlined as a data URL so the topbar/auth-page logo and the favicon
 * render without an extra HTTP request and without an R2 asset deploy.
 */
export const ${exportName} =
  ${JSON.stringify(dataUrl)};
`;
  writeFileSync(outTs, body);
  console.log(`Wrote ${outTs} (${png.length} bytes raw, ${dataUrl.length} chars data URL)`);
}

generate({
  srcPng: resolve(root, "assets", "edgeseo-logo.png"),
  outTs: resolve(root, "frontend-worker", "src", "logo-data-url.ts"),
  exportName: "LOGO_DATA_URL",
  sourceLabel: "assets/edgeseo-logo.png",
});

generate({
  srcPng: resolve(root, "assets", "edgeseo-favicon.png"),
  outTs: resolve(root, "frontend-worker", "src", "favicon-data-url.ts"),
  exportName: "FAVICON_DATA_URL",
  sourceLabel: "assets/edgeseo-favicon.png",
});
