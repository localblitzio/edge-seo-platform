#!/usr/bin/env node
/**
 * Re-generates `frontend-worker/src/logo-data-url.ts` from the
 * resized `assets/edgeseo-logo.png`. Run when the logo changes.
 *
 * Why a generated module instead of inlining bytes at build time?
 *   - Wrangler doesn't bundle binary file imports natively in Workers.
 *   - A generated TS string keeps the data in source where it's easy
 *     to diff, audit, and bundle.
 *   - The PNG is small (~5 KB raw, ~6.4 KB base64) so the bundle hit
 *     is negligible.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const root = resolve(here, "..");
const src = resolve(root, "assets", "edgeseo-logo.png");
const out = resolve(root, "frontend-worker", "src", "logo-data-url.ts");

const png = readFileSync(src);
const dataUrl = `data:image/png;base64,${png.toString("base64")}`;

const body = `/**
 * Generated by \`scripts/regen-logo-data-url.mjs\` from
 * \`assets/edgeseo-logo.png\`. Do not edit by hand — re-run the script
 * to refresh after changing the logo.
 *
 * Inlined as a data URL so the topbar and auth-page logos render
 * without an extra HTTP request and without an R2 asset deploy.
 */
export const LOGO_DATA_URL =
  ${JSON.stringify(dataUrl)};
`;
writeFileSync(out, body);
console.log(`Wrote ${out} (${png.length} bytes raw, ${dataUrl.length} chars data URL)`);
